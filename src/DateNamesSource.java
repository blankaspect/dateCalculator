// IMPORTS


import uk.org.blankaspect.util.StringKeyed;

//----------------------------------------------------------------------


// DATE-NAMES SOURCE ENUMERATION


enum DateNamesSource
    implements StringKeyed
{

////////////////////////////////////////////////////////////////////////
//  Constants
////////////////////////////////////////////////////////////////////////

    LOCALE
    (
        "locale",
        "Locale"
    ),

    USER_DEFINED
    (
        "userDefined",
        "User-defined"
    );

////////////////////////////////////////////////////////////////////////
//  Constructors
////////////////////////////////////////////////////////////////////////

    private DateNamesSource( String key,
                             String text )
    {
        this.key = key;
        this.text = text;
    }

    //------------------------------------------------------------------

////////////////////////////////////////////////////////////////////////
//  Instance methods : StringKeyed interface
////////////////////////////////////////////////////////////////////////

    public String getKey( )
    {
        return key;
    }

    //------------------------------------------------------------------

////////////////////////////////////////////////////////////////////////
//  Instance methods : overriding methods
////////////////////////////////////////////////////////////////////////

    @Override
    public String toString( )
    {
        return text;
    }

    //------------------------------------------------------------------

////////////////////////////////////////////////////////////////////////
//  Instance variables
////////////////////////////////////////////////////////////////////////

    private String  key;
    private String  text;

}

//----------------------------------------------------------------------
